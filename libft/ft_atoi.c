#include "libft.h"

int	ft_atoi(const char *str)
{
	int			min;
	long int	res;

	min = 1;
	res = 0;
	while (*str == ' ' || *str == '\f' || *str == '\n'
		|| *str == '\r' || *str == '\t' || *str == '\v')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			min *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + (*str - '0');
		str++;
		if ((res * min) < ((2147483647 * -1) - 1))
			return (0);
		if ((res * min) > 2147483647)
			return (-1);
	}
	return ((int)res * min);
}
